	</p>
	<p>a. Kiểm tra thông tin sản phẩm: giá, thông tin khuyến mãi. </p>
	<p>b. Chọn số lượng mong muốn. </p>
	<p>c. Thêm sản phẩm vào giỏ hàng.</p>
