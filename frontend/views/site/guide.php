	<p>a. Chọn "Tiếp tục mua hàng" </p>
	<p>b. Thêm sản phẩm vào giỏ hàng như ở Bước 2.</p>
	<p>Sau khi quý khách đã có đủ sản phẩm cần đặt trong giỏ hàng, tiếp tục các bước sau để đặt hàng:</p>
	<p>c. Điều chỉnh số lượng và cập nhật giỏ hàng</p>
	<p>d. Bấm "Thanh toán" để bắt đầu đặt hàng.  </p>
